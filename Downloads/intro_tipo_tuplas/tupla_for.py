#Crea una tupla con los elementos que se desea
Colores = ("rojo", "verde", "azul")
# bucle for para recorrer los elementos de la tupla, iterando sobre ellos de uno en uno
for color in Colores:
#Python crea un iterador que devuelve el siguiente elemento de la tupla en cada iteración, ejecutando el bloque de código asociado al bucle para cada elemento. 
    print(color)
# Al poner el print te debe de dar los mismos colores en el mismo orden pero ahora enlistados 



